// Do while loop will execute the code at least one time.

let x = 100;

do {
  console.log(`${x} - do while loop`);
  x++;
} while (x < 10);

// --------------------------------------------

let a = 100;

while (a < 10) {
  console.log(`${a} - while loop`);
  a++;
}

for (let i = 100; i < 10; i++) {
  console.log(`${i} - for loop`);
}
